package tipoDatos;

public class TiposDatos {

    public static void main(String[] args) {

        //Declaración de variables
        int entero = 7;
        long largo = 7L;
        double doble = 7D;
        boolean siete = true;
        String tipo = "7";

        //Impresión de datos
        System.out.println("Este es un entero: " + entero);
        System.out.println("Este es un long: " + largo);
        System.out.println("Este es un double: " + doble);
        System.out.println("Este es un boolean: " + siete);
        System.out.println("Este es un tipo: " + tipo);
    }
}
